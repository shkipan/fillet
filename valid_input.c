#include "fillit.h"

int ft_is_valid(char *str)
{
	int i;
	int x;
	int ch;

	i = 0;
	x = 0;
	ch = 0;
	while (*(str + i))
	{
		if(*(str + i) == '#')
			ch++;
		if (*(str + i) != '.' && *(str + i) != '#')
		{
			if (*(str + i) != '\n')
				return (0);
			if (((i + 1) % 21 != 0) && ((i + 1 - x) % (5) != 0))
				return (0);
			if (((i + 1) % 21 == 0))
			{
				if(ch > 4)
					return (0);
				ch = 0;
				x++;
			}
		}
		i++;
	}
	if ((x * 21) == i)
		return (x);
	else
		return (0);
}
