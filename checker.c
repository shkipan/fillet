#include "fillit.h"

int ft_is_valid(char **figure)
{
  int i;
  int j;

  i = 0;
  while(i < 4)
  {
    j = 0;
    while (j < 5)
    {
      j++;
    }
    i++;
  }
  figure[0][0] = '1';
  return (1);
}
